// Update chat translations when language changes
const originalTranslatePage = translatePage;
translatePage = function(lang) {
	originalTranslatePage(lang);
    
	// Update chat-specific elements
	const chatElements = document.querySelectorAll('[data-translate-key]');
	chatElements.forEach(el => {
		const key = el.dataset.translateKey;
		if (translations[lang][key]) {
			if (el.tagName === 'INPUT') {
				el.placeholder = translations[lang][key];
			} else {
				el.innerText = translations[lang][key];
			}
		}
	});
};

