let currentLang = 'en'; // Start with English as default language

// --- Translation Function ---
function translatePage(lang) {
	if (!translations[lang]) return;
	currentLang = lang;
	document.documentElement.lang = lang;
	document.title = lang === 'es' ? 
		'Vivero Machin Farms - Homestead FL | Plantas Tropicales' : 
		'Nursery Plants Near Me | Machin Farms Homestead FL';

	// Update meta description dynamically
	const metaDesc = document.querySelector('meta[name="description"]');
	if (metaDesc) {
		metaDesc.content = lang === 'es' ? 
			'Vivero Machin Farms en Homestead FL. Plantas, árboles y flores tropicales. Más de 20 años de experiencia. ¡Visítanos hoy!' :
			'Best nursery plants near me in Homestead FL. Local plant nursery with tropical plants, trees & flowers. Visit Machin Nursery today!';
	}

	document.querySelectorAll('[data-translate-key]').forEach(el => {
		const key = el.dataset.translateKey;
		if (translations[lang][key]) {
			el.innerText = translations[lang][key];
		}
	});
    
	// Update language toggle buttons
	const langToggle = document.getElementById('lang-toggle');
	const langToggleMobile = document.getElementById('lang-toggle-mobile');
	if (langToggle) langToggle.innerText = translations[lang].langToggle;
	if (langToggleMobile) langToggleMobile.innerText = translations[lang].langToggle;
}

// --- Language Toggle Logic ---
function initLanguageToggles() {
	const langToggleBtn = document.getElementById('lang-toggle');
	const langToggleMobileBtn = document.getElementById('lang-toggle-mobile');
	if (langToggleBtn) {
		langToggleBtn.addEventListener('click', () => {
			const newLang = currentLang === 'es' ? 'en' : 'es';
			translatePage(newLang);
		});
	}
	if (langToggleMobileBtn) {
		langToggleMobileBtn.addEventListener('click', () => {
			const newLang = currentLang === 'es' ? 'en' : 'es';
			translatePage(newLang);
		});
	}
}
        
// --- Mobile Menu Logic ---
function initMobileMenu() {
	const mobileMenuButton = document.getElementById('mobile-menu-button');
	const mobileMenu = document.getElementById('mobile-menu');
	if (!mobileMenuButton || !mobileMenu) return;
	const navLinks = mobileMenu.getElementsByTagName('a');

	mobileMenuButton.addEventListener('click', () => {
		mobileMenu.classList.toggle('hidden');
	});

	for (let link of navLinks) {
		link.addEventListener('click', () => {
			mobileMenu.classList.add('hidden');
		});
	}
}

// --- AI Care Modal Logic ---
let modal, modalTitle, modalBody, modalDisclaimer, modalCloseBtn;
function initCareModal() {
	modal = document.getElementById('ai-care-modal');
	modalTitle = document.getElementById('modal-title');
	modalBody = document.getElementById('modal-body');
	modalDisclaimer = document.getElementById('modal-disclaimer');
	modalCloseBtn = document.getElementById('modal-close-btn');
	const careButtons = document.querySelectorAll('.ai-care-btn');

	if (!modal || !modalCloseBtn) return;

	const openModal = () => modal.classList.add('visible');
	const closeModal = () => modal.classList.remove('visible');

	careButtons.forEach(button => {
		button.addEventListener('click', () => {
			const plantName = button.dataset.plantName;
			getPlantCareInfo(plantName, openModal);
		});
	});

	modalCloseBtn.addEventListener('click', closeModal);
	modal.addEventListener('click', (e) => {
		if (e.target === modal) closeModal();
	});
}

