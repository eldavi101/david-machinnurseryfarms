function openChat() {
	const chatWidget = document.getElementById('chat-widget');
	const chatToggle = document.getElementById('chat-toggle');
	if (!chatWidget || !chatToggle) return;
	chatWidget.classList.add('active');
	chatWidget.classList.remove('minimized');
	chatToggle.innerHTML = '<i class="fas fa-times"></i>';
    
	// Focus input when chat opens
	setTimeout(() => {
		const input = document.getElementById('chat-input');
		if (input) input.focus();
	}, 300);
}

function closeChat() {
	const chatWidget = document.getElementById('chat-widget');
	const chatToggle = document.getElementById('chat-toggle');
	if (!chatWidget || !chatToggle) return;
	chatWidget.classList.remove('active');
	chatToggle.innerHTML = '<i class="fas fa-comments"></i>';
}

function addMessage(content, isUser = false, isTyping = false) {
	const messagesContainer = document.getElementById('chat-messages');
	if (!messagesContainer) return null;
	const messageDiv = document.createElement('div');
	messageDiv.className = `chat-message ${isUser ? 'user-message' : 'bot-message'}`;
    
	if (isTyping) {
		messageDiv.innerHTML = `
			<div class="message-content">
				<div class="chat-loading">
					<div class="chat-loading-dot"></div>
					<div class="chat-loading-dot"></div>
					<div class="chat-loading-dot"></div>
				</div>
			</div>
		`;
	} else {
		messageDiv.innerHTML = `<div class="message-content">${content}</div>`;
	}
    
	messagesContainer.appendChild(messageDiv);
	messagesContainer.scrollTop = messagesContainer.scrollHeight;
    
	return messageDiv;
}

function handleChatMessage() {
	const chatInput = document.getElementById('chat-input');
	if (!chatInput) return;
	const message = chatInput.value.trim();
    
	if (!message) return;
    
	// Add user message
	userMessage = message;
	addMessage(message, true);
	chatInput.value = '';
    
	// Show typing indicator
	const typingMessage = addMessage('', false, true);
    
	// After 1.5 seconds, show response and contact form
	setTimeout(() => {
		typingMessage && typingMessage.remove();
        
		const responseMessage = currentLang === 'es' ? 
			'Gracias por tu mensaje! Para responderte mejor, por favor comparte tu información de contacto:' :
			'Thanks for your message! To help you better, please share your contact information:';
        
		addMessage(responseMessage);
        
		// Show contact form
		const contactSection = document.getElementById('contact-form-section');
		if (contactSection) contactSection.style.display = 'block';
        
		// Focus first input
		setTimeout(() => {
			const nameInput = document.getElementById('contact-name');
			if (nameInput) nameInput.focus();
		}, 300);
        
		chatStep = 'contact';
	}, 1500);
}

function handleContactFormSubmission() {
	const form = document.getElementById('chat-contact-form');
	if (!form) return;
	const formData = new FormData(form);
    
	const contactData = {
		name: formData.get('name'),
		email: formData.get('email'),
		phone: formData.get('phone') || 'Not provided',
		message: userMessage
	};

	// Validate email
	if (!isValidEmail(contactData.email)) {
		alert(currentLang === 'es' ? 'Por favor ingresa un email válido' : 'Please enter a valid email');
		return;
	}

	// Disable form
	const submitBtn = form.querySelector('.form-submit-btn');
	if (submitBtn) {
		submitBtn.disabled = true;
		submitBtn.textContent = currentLang === 'es' ? 'Enviando...' : 'Sending...';
	}

	// Send to WhatsApp and Email
	sendToWhatsApp(contactData);
	sendToEmail(contactData);

	// Show success message
	setTimeout(() => {
		const successMessage = currentLang === 'es' ? 
			'¡Perfecto! Te hemos enviado la información por WhatsApp y email. Te contactaremos pronto. 🌱' :
			'Perfect! We\'ve sent your information via WhatsApp and email. We\'ll contact you soon! 🌱';
        
		addMessage(successMessage);
        
		// Hide contact form
		const contactSection = document.getElementById('contact-form-section');
		if (contactSection) contactSection.style.display = 'none';
        
		// Auto-close chat after 3 seconds
		setTimeout(() => {
			closeChat();
		}, 3000);
	}, 1000);
}

