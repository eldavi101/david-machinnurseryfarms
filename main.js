// WebP Support Detection
function supportsWebP() {
	const canvas = document.createElement('canvas');
	canvas.width = 1; canvas.height = 1;
	return canvas.toDataURL('image/webp').indexOf('image/webp') === 5;
}
if (!supportsWebP()) {
	document.documentElement.classList.add('no-webp');
}

function initPerformanceOptimizations() {
	requestAnimationFrame(() => {
		const imageUpdates = [];
		const images = document.querySelectorAll('img:not([width]):not([height])');
		images.forEach(img => {
			const update = { element: img };
			if (img.classList.contains('gallery-img')) {
				update.width = '400';
				update.height = '224';
			} else if (img.classList.contains('h-48')) {
				update.width = '300';
				update.height = '192';
			}
			if (update.width) imageUpdates.push(update);
		});
        
		// Batch write operations
		imageUpdates.forEach(update => {
			update.element.setAttribute('width', update.width);
			update.element.setAttribute('height', update.height);
			update.element.setAttribute('loading', 'lazy');
			update.element.setAttribute('decoding', 'async');
		});
        
		// Preload critical images above the fold
		const criticalImages = [
			'https://i.postimg.cc/w70f22QC/Machin-Farms-LLC-3.png',
			'https://images.unsplash.com/photo-1520454224216-5cd2b9084153?q=80&w=1200&auto=format&fit=crop&fm=webp'
		];
        
		criticalImages.forEach(src => {
			const link = document.createElement('link');
			link.rel = 'preload';
			link.as = 'image';
			link.href = src;
			document.head.appendChild(link);
		});
	});
}

